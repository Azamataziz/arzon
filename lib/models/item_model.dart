class ItemModel {
  final String image;
  final String firstPrice;
  final String secondPrice; 
  final String sale;
  final String title;
  final String count;
  final String endDate;
  final String brand;

  const ItemModel({
    required this.image,
    required this.firstPrice,
    required this.secondPrice,
    required this.sale,
    required this.title,
    required this.count,
    required this.endDate,
    required this.brand,
  });
}
